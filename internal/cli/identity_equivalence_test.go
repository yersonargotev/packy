func assertFreshPackyOwnershipPreservesLegacyMatty(t *testing.T) {
	t.Helper()
	opts, _, home := sandboxOptions(t)
	env := opts.Env.(MapEnv)
	legacy := map[string]string{
		filepath.Join(home, ".matty", "config.json"):                  "legacy-state\n",
		filepath.Join(home, ".matty", "packs.json"):                   "legacy-packs\n",
		filepath.Join(home, ".local", "share", "matty", "sentinel"):   "legacy-source\n",
		filepath.Join(home, ".codex", "AGENTS.md"):                    "<!-- matty:skills-router -->\nlegacy marker\n<!-- /matty:skills-router -->\n",
		filepath.Join(env["XDG_CONFIG_HOME"], "opencode", "matty.md"): "legacy reference\n",
	}
	for path, content := range legacy {
		if err := os.MkdirAll(filepath.Dir(path), 0o700); err != nil {
			t.Fatal(err)
		}
		if err := os.WriteFile(path, []byte(content), 0o600); err != nil {
			t.Fatal(err)
		}
	}
	env["MATTY_SKILLS_SOURCE"] = createSkillSource(t)
	if out, err := executeCommand(t, NewRootCommand(opts), "install"); err != nil {
		t.Fatalf("install: %v\n%s", err, out)
	}
	if out, err := executeCommand(t, NewRootCommand(opts), "doctor", "--json"); err != nil {
		t.Fatalf("doctor: %v\n%s", err, out)
	}
	if out, err := executeCommand(t, NewRootCommand(opts), "update", "--dry-run"); err != nil {
		t.Fatalf("update dry-run: %v\n%s", err, out)
	}
	if out, err := executeCommand(t, NewRootCommand(opts), "uninstall"); err != nil {
		t.Fatalf("uninstall: %v\n%s", err, out)
	}
	for path, want := range legacy {
		got, err := os.ReadFile(path)
		if err != nil || string(got) != want {
			t.Fatalf("legacy Matty artifact changed: %s: %q, %v", path, got, err)
		}
	}
	if _, err := os.Stat(filepath.Join(home, ".packy", "config.json")); !os.IsNotExist(err) {
		t.Fatalf("fresh Packy state survived uninstall: %v", err)
	}
}
