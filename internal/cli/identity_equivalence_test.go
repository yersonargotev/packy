func identityEvidenceSnapshot(t *testing.T, root, product string, roots map[string]string) string {
	t.Helper()
	type fact struct {
		Path    string `json:"path"`
		Kind    string `json:"kind"`
		Content string `json:"content,omitempty"`
		Target  string `json:"target,omitempty"`
	}
	var entries []fact
	err := filepath.WalkDir(root, func(path string, entry os.DirEntry, walkErr error) error {
		if walkErr != nil {
			return walkErr
		}
		rel, err := filepath.Rel(root, path)
		if err != nil {
			return err
		}
		if entry.IsDir() && entry.Name() == ".git" {
			return filepath.SkipDir
		}
		info, err := os.Lstat(path)
		if err != nil {
			return err
		}
		switch {
		case info.Mode()&os.ModeSymlink != 0:
			target, err := os.Readlink(path)
			if err != nil {
				return err
			}
			entries = append(entries, fact{Path: normalizeIdentityEvidence(rel, product, roots), Kind: "symlink", Target: normalizeIdentityEvidence(target, product, roots)})
		case entry.IsDir():
			entries = append(entries, fact{Path: normalizeIdentityEvidence(rel, product, roots), Kind: "directory"})
		default:
			data, err := os.ReadFile(path)
			if err != nil {
				return err
			}
			if rel == filepath.Join("."+product, "packs.json") {
				data = []byte(normalizeIdentityStateEvolution(string(data)))
			}
			entries = append(entries, fact{Path: normalizeIdentityEvidence(rel, product, roots), Kind: "file", Content: normalizeIdentityEvidence(string(data), product, roots)})
		}
		return nil
	})
	if err != nil {
		t.Fatalf("snapshot identity evidence: %v", err)
	}
	sort.Slice(entries, func(i, j int) bool { return entries[i].Path < entries[j].Path })
	data, err := json.Marshal(entries)
	if err != nil {
		t.Fatalf("marshal identity evidence: %v", err)
	}
	return string(data)
}

func normalizeIdentityStateEvolution(value string) string {
	value = strings.ReplaceAll(value, `"schema_version": 2`, `"schema_version": 1`)
	value = strings.Replace(value, `"schema_version": 3`, `"schema_version": 2`, 1)
	return identityEmptyAliasesRE.ReplaceAllString(value, "")
}

